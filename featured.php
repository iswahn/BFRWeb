			$return = "";
			foreach (glob($path . "/*sm.*") as $filename)
			{
				list($name, $extension) = split("\.", $filename, 2);
				$return .= '<a class="fancybox-thumbs'.$count.'" data-fancybox-group="thumb" href="'.substr($name,0,-3).'.'.$extension.'">';
